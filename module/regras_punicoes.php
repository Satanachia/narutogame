<div class="titulo-secao"><p>Regras e Punições</p></div>
  <div class="conteudo-direita">
    <p>
      O Naruto Game conta com regras simples, mas necessárias para o convívio ideal  entre os membros do jogo. Regras fundamentais e importantes, para melhor orientar a conduta do jogador.  Se houver dúvidas se estará infringindo alguma delas, entre em contato com a STAFF, e espere ter a resposta. No decorrer do jogo, podemos adicionar novas regras e punições, dependendo dos acontecimentos.<br><br>

      Os jogadores que não as cumprirem, ignorarem ou simplesmente tentar  burlá-las, poderão ser punidos de acordo com as regras, que podem ser alteradas, sem aviso prévio.<br><br>

      Temos como base, dois conceitos simples, que acabam abrindo um leque de opções dentro de cada uma delas:<br><br>

      - Os jogadores não podem utilizar métodos imprevistos no jogo, que dêem vantagens  em relação aos outros jogadores ou crie desvantagens para os outros jogadores.<br>
      - Ofensas no geral, contra outros jogadores e administradores do NG não são permitidas. Sobre ofensas, palavras, nomes e imagens estão inclusas.<br><br>

      São regras bem simples, que não há motivos para  a falta de compreensão ou descumprí-las. Mas podemos deixar alguns termos, com uma melhor compreensão:<br><br>

      <b>• MULTI:</b> Multi é o mesmo que ter mais de uma conta, para tirar vantagem. No NG é permitido ter mais de uma conta, contudo se você usar para ajudar na pontuação de um personagem principal, com batalhas,  isto JAMAIS será permitido. Caso você tenha uma conta e compartilhe o mesmo computador com um irmão ou outro parente, deve nos informar imediatamente, pois todos de sua família podem jogar o Naruto Game, mas em momento algum, devem BATALHAR ENTRE SÍ. <br><br>

      <b>• DIVISÃO DE CONTAS:</b> Não aconselhamos que se divida a conta com outros jogadores, se por algum motivo algo aconteça na conta que é dividida, nós NÃO iremos interferir. E há o risco de sua conta ser usada por outra pessoa que tenha o acesso, para fazer multi, que será banido.<br><br>

      <b>• VITÓRIAS COMBINADAS (SWAP):</b> Troca de vitórias é um meio de tirar vantagem, de forma desonesta, e totalmente cabível à punição.<br><br>

      <b>• ROBÔS (BOTS):</b> Quando um jogador encontrar algum programa que prometa dar vantagens para o seu personagem, é importante saber que este programa não está sendo tão bonzinho a ponto de apenas dar as coisas. Ele irá pegar sua senha, deletando sua conta a seguir, e mais importante, ele pode armazenar diversas informações importantes que estão em seu computador, e assim que descoberto, poderá ser punido pelos Administradores já que usar estes bots não é permitido. Informe-nos imediatamente assim que encontrar algum destes programas. <br><br>

      <b>• CONTEÚDO OFENSIVO:</b> Conteúdos ofensivos incluem qualquer mensagem de texto e/ou imagem que agrida os membros ou possa a vir a agredir. Imagens com tópicos religiosos e semelhantes devem ser evitados ao máximo e em caso de reclamação por parte de alguém, será editado. Imagens e textos pornográficos são proibidos. Quando a agressão for não intencional ela não será levada em conta, desde que seja corrigida assim que solicitado. No caso de reincidência ou conteúdo com objeto claro de ofender haverá punição que pode ser aplicada somente sobre o líder ou todos os membros da equipe e/ou organização, dependendo do julgamento. O conteúdo ofensivo se aplica para nomes de personagens, equipes, organizações, fóruns, mensagens, comunicados e semelhantes.<br><br>

      <b>• NOMES:</b> Não é permitido o uso de nome que seja referente à STAFF ou administração do jogo, nomes ofensivos, homofóbicos ou de cunho sexual, tanto para jogadores, equipes ou organizações.<br><br>

      <b>• CALÚNIA:</b> Ao for comprovado que um jogador deseja difamar alguém, o mesmo será punido severamente, pois não aceitaremos esta atitude desrespeitosa de criar má fama há algum jogador sem motivo.<br><br>

      <b>PUNIÇÕES</b><br><br>

      Sobre as punições, vale ressaltar que podemos, a qualquer momento, de acordo com o grau de conduta do jogador, pular alguma etapa de punição ou mudar a punição, de acordo com a decisão da STAFF.<br><br>
      <b>
      *Advertência <br>
      *Suspensão de Personagem 3 dias<br>
      *Suspensão de Personagem 7 dias<br>
      *Suspensão Temporária de Personagem, tempo à definir pela STAFF<br>
      *Suspensão de Personagem no Round Ativo<br>
      *Banimento Permanente de Personagem<br>
      *Suspensão Temporária da Conta, tempo à definir pela STAFF<br>
      *Banimento Permanente da Conta<br>
      </b><br><br>
      Ainda há a possibilidade, se reconhecer que houve ganho de experiência, habilidades, itens ou qualquer outra coisa ganha de forma ilegal dentro do jogo, poderá ser retirado do personagem.<br><br>

      <b>Multis / SWAP:</b><br><br>

      <b>1ª incidência</b> = Personagens envolvidos serão suspensos no Round Ativo.<br>
      <b>2ª incidência</b> = Personagens envolvidos serão suspensos no Round Ativo e a Conta será suspensa, tempo a definir pela STAFF.<br>
      <b>3ª incidência</b> = Banimento Permanente da Conta e dos Personagens.<br><br>

      <b>Ofensas / Ameaças / Calúnias:</b> <br><br>

      <b>1ª incidência</b> = Advertência.<br>
      <b>2ª incidência</b> = Suspensão de no mínimo 03 dias.<br>
      <b>3ª incidência</b> = Suspensão de no mínimo 07 dias.<br>
      <b>4ª incidência</b> =  Suspensão de Personagem no Round Ativo.<br>
      <b>5ª incidência</b> = Suspensão Temporária da Conta, tempo à definir pela STAFF.<br>
      <b>6ª incidência</b> = Banimento Permanente da Conta.<br><br>

      <b>Utilização de robôs, bugs e táticas não previstas:</b> <br><br>

      <b>1ª incidência</b> = suspensão de no mínimo 03 dias mais punição de acordo com irregularidade praticada pelo jogador.<br>
      <b>2ª incidência</b> = Personagem envolvido será suspenso no Round Ativo.<br>
      <b>3ª incidência</b> = Suspensão Temporária da Conta, tempo à definir pela STAFF.<br>
      <b>4ª incidência</b> = Banimento Permanente da Conta.<br><br>

      Qualquer dúvida, entrar em contato com a STAFF.

